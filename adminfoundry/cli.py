@extensions_app.command("list")
def extensions_list():
    """List all registered extensions."""
    from adminfoundry.extensions import extension_registry
    exts = extension_registry.all()
    if not exts:
        typer.echo("No extensions registered.")
        return
    typer.echo(f"{'Name':<25} {'Version':<10} {'Optional'}")
    typer.echo("-" * 45)
    for ext in exts:
        typer.echo(f"{ext.name:<25} {ext.version:<10} {ext.is_optional}")


@extensions_app.command("check")
def extensions_check():
    """Run startup checks for all registered extensions."""
    from adminfoundry.extensions import extension_registry
    exts = extension_registry.all()
    if not exts:
        typer.echo("No extensions registered.")
        return
    all_ok = True
    for ext in exts:
        try:
            ext.startup_check()
            typer.echo(f"  {ext.name}: OK")
        except RuntimeError as exc:
            if not ext.is_optional:
                typer.echo(f"  {ext.name}: FAIL (required) — {exc}", err=True)
                all_ok = False
            else:
                typer.echo(f"  {ext.name}: WARN (optional) — {exc}")
    if not all_ok:
        raise typer.Exit(code=1)


