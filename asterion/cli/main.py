@app.command()
def doctor(
    app_spec: str = typer.Option(
        "",
        "--app",
        envvar="ASTERION_APP",
        help=(
            "Optional 'module:attribute' pointing at the FastAPI app. "
            "Enables the [REG] registry check."
        ),
    ),
):
    """Verify config, DB connectivity, permission catalog, and (with --app) registry health."""
    asyncio.run(_doctor(app_spec))


async def _doctor(app_spec: str) -> None:
    failures: list[str] = []

    typer.echo("[CFG] Loading CoreAdminConfig.from_env() ...")
    try:
        config = CoreAdminConfig.from_env()
    except ValueError as exc:
        typer.echo(f"[CFG] FAIL — {exc}", err=True)
        raise typer.Exit(code=1) from exc
    typer.echo(f"[CFG] OK (environment={config.environment})")

    typer.echo("[VER] asterion version ...")
    try:
        import asterion as _af

        typer.echo(f"[VER] OK ({_af.__version__})")
    except Exception as exc:
        typer.echo(f"[VER] WARN — {exc}")

    typer.echo("[DB]  Checking database connectivity ...")
    db = _make_db(config)
    try:
        async with db.engine.begin() as conn:
            await conn.execute(text("SELECT 1"))
        typer.echo("[DB]  OK")
    except Exception as exc:
        typer.echo(f"[DB]  FAIL — {exc}", err=True)
        failures.append("db")

    typer.echo("[CAT] PermissionCatalog populated ...")
    try:
        from sqlalchemy import func as _func

        from asterion.models.permission_catalog import PermissionCatalog

        async with db.session() as session:
            count = (await session.execute(_func.count(PermissionCatalog.id).select())).scalar_one()
        if count == 0:
            typer.echo(
                "[CAT] WARN — PermissionCatalog is empty. "
                "Run `asterion permissions sync --app <module:attr>`."
            )
        else:
            typer.echo(f"[CAT] OK ({count} permission key(s))")
    except Exception as exc:
        typer.echo(f"[CAT] WARN — {exc}")

    typer.echo("[MT]  Multi-tenant config ...")
    if config.enable_multi_tenant:
        if not config.tenant_header_name.strip():
            typer.echo("[MT]  FAIL — tenant_header_name empty", err=True)
            failures.append("multi-tenant header")
        elif config.database_url.startswith(("sqlite://", "sqlite+aiosqlite://")):
            typer.echo(
                "[MT]  WARN — multi-tenant on SQLite is dev-only; isolation requires PostgreSQL."
            )
        else:
            typer.echo(
                f"[MT]  OK (resolution={config.tenant_resolution}, header={config.tenant_header_name})"
            )
    else:
        typer.echo("[MT]  OK (disabled)")

    typer.echo("[CORS] CORS config ...")
    if not config.cors_origins:
        typer.echo("[CORS] OK (middleware not installed; no origins configured)")
    else:
        if config.cors_allow_credentials and "*" in config.cors_origins:
            typer.echo("[CORS] FAIL — cors_origins=['*'] + credentials=True", err=True)
            failures.append("cors")
        else:
            typer.echo(f"[CORS] OK ({len(config.cors_origins)} origin(s))")

    typer.echo("[REG] Registered resources ...")
    if not app_spec:
        typer.echo("[REG] SKIP (pass --app or set ASTERION_APP to enable)")
    else:
        try:
            registry = _load_registry_from_app_spec(app_spec)
            names = registry.model_names()
            if not names:
                typer.echo("[REG] WARN — registry is empty.")
            else:
                typer.echo(f"[REG] OK ({len(names)} resource(s): {', '.join(sorted(names))})")
        except typer.Exit:
            failures.append("registry")
        except Exception as exc:
            typer.echo(f"[REG] WARN — {exc}")

    await db.dispose()

    if failures:
        typer.echo(f"\ndoctor: FAILED — {', '.join(failures)}", err=True)
        raise typer.Exit(code=1)
    typer.echo("\ndoctor: all checks passed.")


