@app.command()
def init(
    directory: str = typer.Argument(".", help="Target directory for the scaffold"),
):
    """Create a minimal asterion app scaffold in the given directory."""
    base = Path(directory)

    scaffold = '''\
"""Minimal asterion app — generated by `asterion init`."""
from asterion import CoreAdminConfig, create_admin

app = create_admin(config=CoreAdminConfig.from_env())
'''

    env_example = """\
ASTERION_DATABASE_URL=postgresql+asyncpg://postgres:postgres@localhost:5432/myapp
# Required. Generate via: openssl rand -hex 32
ASTERION_SECRET_KEY=
"""

    for path, content in [(base / "app.py", scaffold), (base / ".env.example", env_example)]:
        if not path.exists():
            path.write_text(content)
            typer.echo(f"Created {path}")
        else:
            typer.echo(f"Skipped {path} (already exists)")

    typer.echo("\nasterion scaffold ready. Next steps:")
    typer.echo("  1. Set ASTERION_DATABASE_URL and ASTERION_SECRET_KEY")
    typer.echo("  2. asterion db upgrade")
    typer.echo("  3. asterion create-superadmin")
    typer.echo("  4. uvicorn app:app --reload")


@app.command("create-superadmin")
def create_superadmin(
    email: str = typer.Option(..., prompt=True),
    password: str = typer.Option(..., prompt=True, hide_input=True, confirmation_prompt=True),
    full_name: str | None = typer.Option(None),
):
    """Create the first superadmin user (idempotent by email)."""
    asyncio.run(_create_superadmin(email, password, full_name))


async def _create_superadmin(email: str, password: str, full_name: str | None) -> None:
    config = _load_config()
    db = _make_db(config)
    try:
        async with db.session() as session:
            async with session.begin():
                existing = await _find_user_by_email(session, email)
                if existing is not None:
                    existing.is_superadmin = True
                    existing.is_active = True
                    typer.echo(f"Promoted existing user {email} to superadmin.")
                    return
                session.add(
                    User(
                        email=email,
                        hashed_password=hash_password(password),
                        full_name=full_name,
                        is_active=True,
                        is_superadmin=True,
                    )
                )
        typer.echo(f"Superadmin {email} created.")
    finally:
        await db.dispose()


