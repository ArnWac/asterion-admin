@tenant_app.command("create")
def tenant_create(
    name: str = typer.Option(..., "--name", help="Display name of the tenant"),
    slug: str = typer.Option(..., "--slug", help="URL-safe tenant slug"),
    owner_email: str | None = typer.Option(
        None,
        "--owner-email",
        help="Email of the owner user. If --create-owner is set, the user is "
        "created when missing; otherwise the user must already exist.",
    ),
    create_owner: bool = typer.Option(
        False,
        "--create-owner",
        help="Create the owner user if not found. Requires --owner-password.",
    ),
    owner_password: str | None = typer.Option(
        None,
        "--owner-password",
        help="Password used when creating a new owner user.",
    ),
    owner_full_name: str | None = typer.Option(
        None, "--owner-full-name", help="Optional full name for a new owner user."
    ),
    skip_schema: bool = typer.Option(
        False,
        "--skip-schema",
        help="Skip PostgreSQL schema provisioning (useful for tests).",
    ),
):
    """Create a tenant. Idempotent: re-running on an existing slug updates."""
    asyncio.run(
        _tenant_create(
            name=name,
            slug=slug,
            owner_email=owner_email,
            create_owner=create_owner,
            owner_password=owner_password,
            owner_full_name=owner_full_name,
            skip_schema=skip_schema,
        )
    )


async def _tenant_create(
    *,
    name: str,
    slug: str,
    owner_email: str | None,
    create_owner: bool,
    owner_password: str | None,
    owner_full_name: str | None,
    skip_schema: bool,
) -> None:
    try:
        slug = validate_tenant_slug(slug)
    except ValidationError as exc:
        typer.echo(f"Invalid slug: {exc}", err=True)
        raise typer.Exit(code=2) from exc

    config = _load_config()
    db = _make_db(config)

    owner_membership_id = None
    try:
        async with db.session() as session:
            async with session.begin():
                tenant = await create_tenant_record(session, name=name, slug=slug)

                if owner_email:
                    owner = await _find_user_by_email(session, owner_email)
                    if owner is None:
                        if not create_owner:
                            typer.echo(
                                f"Owner user {owner_email!r} not found. "
                                "Re-run with --create-owner --owner-password to create it.",
                                err=True,
                            )
                            raise typer.Exit(code=2)
                        if not owner_password:
                            typer.echo(
                                "--owner-password is required when --create-owner is set.",
                                err=True,
                            )
                            raise typer.Exit(code=2)
                        owner = User(
                            email=owner_email,
                            hashed_password=hash_password(owner_password),
                            full_name=owner_full_name,
                            is_active=True,
                            is_superadmin=False,
                        )
                        session.add(owner)
                        await session.flush()

                    membership = await assign_owner_membership(session, tenant=tenant, user=owner)
                    owner_membership_id = membership.id

        if not skip_schema:
            async with db.session() as session:
                await bootstrap_tenant(
                    slug,
                    session,
                    owner_membership_id=owner_membership_id,
                    database_url=config.database_url,
                )

        typer.echo(f"Tenant {slug!r} ready.")
    finally:
        await db.dispose()


@tenant_app.command("list")
def tenant_list():
    """List all tenants in the public schema."""
    asyncio.run(_tenant_list())


async def _tenant_list() -> None:
    config = _load_config()
    db = _make_db(config)
    try:
        async with db.session() as session:
            result = await session.execute(select(Tenant).order_by(Tenant.slug))
            tenants = result.scalars().all()
    finally:
        await db.dispose()

    if not tenants:
        typer.echo("No tenants registered.")
        return

    typer.echo(f"{'SLUG':<24} {'NAME':<32} {'SCHEMA':<24} ACTIVE")
    for tenant in tenants:
        typer.echo(
            f"{tenant.slug:<24} {tenant.name:<32} {tenant.schema_name:<24} {tenant.is_active}"
        )


@tenant_app.command("bootstrap")
def tenant_bootstrap(slug: str = typer.Argument(..., help="Tenant slug")):
    """Re-run schema provisioning and role seeding for an existing tenant.

    Idempotent. Safe to run after schema migrations or to repair a tenant.
    """
    asyncio.run(_tenant_bootstrap(slug))


async def _tenant_bootstrap(slug: str) -> None:
    try:
        slug = validate_tenant_slug(slug)
    except ValidationError as exc:
        typer.echo(f"Invalid slug: {exc}", err=True)
        raise typer.Exit(code=2) from exc

    config = _load_config()
    db = _make_db(config)
    try:
        async with db.session() as session:
            tenant = (
                await session.execute(select(Tenant).where(Tenant.slug == slug))
            ).scalar_one_or_none()
            if tenant is None:
                typer.echo(
                    f"Tenant {slug!r} not found. Run `asterion tenant create` first.",
                    err=True,
                )
                raise typer.Exit(code=2)

        async with db.session() as session:
            await bootstrap_tenant(slug, session, database_url=config.database_url)

        typer.echo(f"Tenant {slug!r} bootstrapped.")
    finally:
        await db.dispose()


@tenant_app.command("upgrade")
def tenant_upgrade(
    all: bool = typer.Option(False, "--all", help="Upgrade all active tenants"),
    slug: str | None = typer.Option(None, "--slug", help="Single tenant slug"),
):
    """Re-bootstrap one tenant (--slug) or every active tenant (--all)."""
    if all:
        asyncio.run(_tenant_upgrade_all())
    elif slug:
        asyncio.run(_tenant_bootstrap(slug))
    else:
        typer.echo("Provide --all or --slug <slug>.", err=True)
        raise typer.Exit(code=2)


async def _tenant_upgrade_all() -> None:
    config = _load_config()
    db = _make_db(config)
    try:
        async with db.session() as session:
            result = await session.execute(
                select(Tenant).where(Tenant.is_active == True)  # noqa: E712
            )
            tenants = list(result.scalars().all())
    finally:
        await db.dispose()

    if not tenants:
        typer.echo("No active tenants found.")
        return

    for tenant in tenants:
        await _tenant_bootstrap(tenant.slug)


@tenant_app.command("disable")
def tenant_disable(slug: str = typer.Argument(..., help="Tenant slug")):
    """Mark a tenant inactive. Reversible via ``tenant enable``."""
    asyncio.run(_tenant_set_active(slug, active=False))


@tenant_app.command("enable")
def tenant_enable(slug: str = typer.Argument(..., help="Tenant slug")):
    """Mark a tenant active."""
    asyncio.run(_tenant_set_active(slug, active=True))


async def _tenant_set_active(slug: str, *, active: bool) -> None:
    try:
        slug = validate_tenant_slug(slug)
    except ValidationError as exc:
        typer.echo(f"Invalid slug: {exc}", err=True)
        raise typer.Exit(code=2) from exc

    config = _load_config()
    db = _make_db(config)
    try:
        async with db.session() as session:
            async with session.begin():
                tenant = (
                    await session.execute(select(Tenant).where(Tenant.slug == slug))
                ).scalar_one_or_none()
                if tenant is None:
                    typer.echo(f"Tenant {slug!r} not found.", err=True)
                    raise typer.Exit(code=2)
                if tenant.is_active is active:
                    typer.echo(f"Tenant {slug!r} already {'enabled' if active else 'disabled'}.")
                    return
                tenant.is_active = active
        typer.echo(f"Tenant {slug!r} {'enabled' if active else 'disabled'}.")
    finally:
        await db.dispose()


