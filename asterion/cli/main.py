APP_SPEC_ENV = "ASTERION_APP"


def _load_runtime_from_app_spec(app_spec: str):
    """Import ``module:attribute`` and return the ``AdminRuntime`` behind it.

    ``attribute`` may be either the FastAPI ``app`` instance or a zero-arg
    factory callable that returns one. A FastAPI instance is itself callable
    (as an ASGI app), so we check for the runtime first and only fall through
    to ``attr()`` for genuine factories.
    """
    from fastapi import FastAPI

    if ":" not in app_spec:
        typer.echo(
            f"Invalid --app value: {app_spec!r}. Expected 'module:attribute'.",
            err=True,
        )
        raise typer.Exit(code=2)
    module_name, attr_name = app_spec.split(":", 1)
    try:
        module = importlib.import_module(module_name)
    except ImportError as exc:
        typer.echo(f"Cannot import module {module_name!r}: {exc}", err=True)
        raise typer.Exit(code=2) from exc
    attr = getattr(module, attr_name, None)
    if attr is None:
        typer.echo(
            f"Module {module_name!r} has no attribute {attr_name!r}.",
            err=True,
        )
        raise typer.Exit(code=2)

    if isinstance(attr, FastAPI):
        fastapi_app = attr
    elif callable(attr):
        fastapi_app = attr()
    else:
        fastapi_app = attr

    runtime = getattr(getattr(fastapi_app, "state", None), "asterion", None)
    if runtime is None:
        typer.echo(
            f"{app_spec!r} does not look like an asterion app (missing app.state.asterion).",
            err=True,
        )
        raise typer.Exit(code=2)
    return runtime


def _load_registry_from_app_spec(app_spec: str):
    """Convenience wrapper — returns just the AdminRegistry."""
    return _load_runtime_from_app_spec(app_spec).registry


