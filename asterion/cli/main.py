@user_app.command("list")
def user_list(
    inactive: bool = typer.Option(False, "--inactive", help="Include inactive users only."),
    superadmin: bool = typer.Option(False, "--superadmin", help="Only superadmins."),
    search: str | None = typer.Option(
        None, "--search", help="Substring match against email or full_name."
    ),
):
    """List users (defaults to all)."""
    asyncio.run(_user_list(inactive=inactive, superadmin=superadmin, search=search))


async def _user_list(*, inactive: bool, superadmin: bool, search: str | None) -> None:
    config = _load_config()
    db = _make_db(config)
    try:
        async with db.session() as session:
            stmt = select(User).order_by(User.email)
            if inactive:
                stmt = stmt.where(User.is_active == False)  # noqa: E712
            if superadmin:
                stmt = stmt.where(User.is_superadmin == True)  # noqa: E712
            if search:
                needle = f"%{search.strip()}%"
                from sqlalchemy import or_

                stmt = stmt.where(or_(User.email.ilike(needle), User.full_name.ilike(needle)))
            rows = (await session.execute(stmt)).scalars().all()
    finally:
        await db.dispose()

    if not rows:
        typer.echo("No users match.")
        return

    typer.echo(f"{'EMAIL':<32} {'FULL NAME':<24} ACTIVE SUPER")
    for u in rows:
        typer.echo(f"{u.email:<32} {(u.full_name or '-'):<24} {u.is_active!s:<6} {u.is_superadmin}")


