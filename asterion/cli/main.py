@user_app.command("disable")
def user_disable(email: str = typer.Option(..., "--email")):
    """Mark a user inactive AND revoke every existing access token.

    Implementation: sets ``is_active=False`` and bumps ``token_version``
    so any in-flight token starts failing the ``tkv`` check.
    """
    asyncio.run(_user_set_active(email=email, active=False))


@user_app.command("enable")
def user_enable(email: str = typer.Option(..., "--email")):
    """Re-enable a previously disabled user. Does NOT issue tokens."""
    asyncio.run(_user_set_active(email=email, active=True))


async def _user_set_active(*, email: str, active: bool) -> None:
    config = _load_config()
    db = _make_db(config)
    try:
        async with db.session() as session:
            async with session.begin():
                user = await _find_user_by_email(session, email)
                if user is None:
                    typer.echo(f"User {email!r} not found.", err=True)
                    raise typer.Exit(code=2)
                if user.is_active is active:
                    typer.echo(f"User {email!r} already {'enabled' if active else 'disabled'}.")
                    return
                user.is_active = active
                if not active:
                    # Disabling revokes existing tokens too.
                    user.token_version = (user.token_version or 0) + 1
        typer.echo(f"User {email} {'enabled' if active else 'disabled'}.")
    finally:
        await db.dispose()


